public class Person {
    private String name;
    private String phone_no;
    private String email;
    private String address;
    private String ssn; // Social Security number
    private String dob;

    public Person(String name, String phone_no, String email, String address, String ssn, String dob) {
        this.name = name;
        this.phone_no = phone_no;
        this.email = email;
        this.address = address;
        this.ssn = ssn;
        this.dob = dob;
    }
}
